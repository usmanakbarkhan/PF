num1=float(input("enter a number1:"))
num2=float(input("enter a number2:"))
operator=input("enter a operator (+,-,*,/):")
if(operator=='+'):
    print(num1 + num2)
elif(operator=='-'):
    print(num1 - num2)
elif(operator=='*'):
    print(num1 * num2)
elif(operator=='/'):
    print(num1 / num2)